package data;

/**
 *
 * @author ACER
 */

public abstract class Tempat {
    private String nama;
    private String alamat;

    public Tempat(String nama, String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public abstract String info(); // Abstraksi untuk informasi umum tempat
}
